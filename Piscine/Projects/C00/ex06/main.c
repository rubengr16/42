#include <unistd.h>

void	initialice(char c[])
{
	c[2] = ' ';
	c[5] = ',';
	c[6] = ' ';
	c[0] = '0';
	c[1] = '0';
}

void	print(const char c[])
{
	if (c[0] == '9' && c[1] == '8' && c[3] == '9' && c[4] == '9')
		write(1, c, 5);
	if ((c[0] != c[3] || c[1] != c[4])
		&& (c[0] != '9' && c[1] != '8' && c[3] != '9' && c[4] != '9'))
		write(1, c, 7);
}

void	ft_print_comb2(void)
{
	char	c[7];

	initialice(c);
	while (c[0] <= '9' && c[1] <= '9')
	{
		c[3] = c[0];
		c[4] = c[1] + 1;
		while (c[3] <= '9' && c[4] <= '9')
		{
			print(c);
			c[4]++;
			if (c[4] > '9')
			{
				c[4] = '0';
				c[3]++;
			}
		}
		c[1]++;
		if (c[1] > '9')
		{
			c[1] = '0';
			c[0]++;
		}
	}
}

int	main(void)
{
	ft_print_comb2();
}
